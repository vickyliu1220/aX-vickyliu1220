city1 = "Toronto" 
city2 = "Seattle"

# Relational operator: Equal 
test = (city1 == city2)
print(test)

#Relational operator: Not equal
test = (city1 != city2)
print(test)

#Relational operator: Greater than
test = (city1 > city2)
print(test)

#Relational operator: Less than
test = (city1 < city2)
print(test)
